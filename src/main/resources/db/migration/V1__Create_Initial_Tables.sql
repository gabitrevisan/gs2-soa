-- Cria as tabelas USERS e TRILHAS no schema do Oracle

-- Tabela de Usuários
CREATE TABLE USUARIOS (
    ID NUMBER(19,0) GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
    NOME VARCHAR2(100) NOT NULL,
    EMAIL VARCHAR2(150) UNIQUE NOT NULL,
    AREA_ATUACAO VARCHAR2(100),
    NIVEL_CARREIRA VARCHAR2(50),
    -- Data de cadastro omitida por brevidade na model, mas é bom tê-la.
    -- DATA_CADASTRO DATE DEFAULT SYSDATE,
    CONSTRAINT PK_USUARIOS PRIMARY KEY (ID)
);

-- Tabela de Trilhas de Aprendizagem
CREATE TABLE TRILHAS (
    ID NUMBER(19,0) GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
    NOME VARCHAR2(150) NOT NULL,
    DESCRICAO CLOB,
    NIVEL VARCHAR2(50) NOT NULL,
    CARGA_HORARIA NUMBER(10,0) NOT NULL,
    FOCO_PRINCIPAL VARCHAR2(100),
    CONSTRAINT PK_TRILHAS PRIMARY KEY (ID)
);

-- Note: O CLOB é usado para campos TEXT em Oracle.
-- O "NUMBER(19,0) GENERATED BY DEFAULT AS IDENTITY" é o padrão moderno no Oracle para simular AUTO_INCREMENT.